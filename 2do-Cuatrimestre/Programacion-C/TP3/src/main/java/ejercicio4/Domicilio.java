package ejercicio4;

public class Domicilio {
    private String calle;
    private int numero;
    private String ciudad;
    private String provincia;

    public Domicilio(String calle, int numero, String ciudad, String provincia) {
        this.calle = calle;
        this.numero = numero;
        this.ciudad = ciudad;
        this.provincia = provincia;
    }


}
